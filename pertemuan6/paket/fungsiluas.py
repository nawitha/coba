def balok_luas(x,y,z):
    luas_balok= 2 * (x*y+x*z+y*z)
    return luas_balok   

def kubus_luas(x):
    luas_kubus = 6*(x**2)
    return luas_kubus  

def limas_segiempat_luas(x,y):
    luas_limassegiempat=0,5*x*y
    return luas_limassegiempat

def bola_luas(x):
    luas_bola=4*3,14*(x**2)
    return luas_bola